    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Orders orders = new Orders();
        String lines;
        int y = 0;
        
        try{
            FileReader fr = new FileReader("/Users/yongpua/Desktop/Java/Java Assignment/src/apu/cafeteria/Order.txt");
            BufferedReader order = new BufferedReader(fr);
            try{
                while((lines = order.readLine()) != null){
                    String[] split = lines.split(" , ");
                    
                    if (id.equals(split[0])){
                        jOrders.setText(Arrays.toString(split));
                        y = 1;
                    }     
                }
                if (y == 0){ 
                    JOptionPane.showMessageDialog(orders, "No Orders Detected. Please Try Again.");
                }
                
                }catch(IOException e){
                     JOptionPane.showMessageDialog(orders, "No input detected. Please Try Again.");
                }
 
                
        } catch(FileNotFoundException e){
            JOptionPane.showMessageDialog(orders, "Orders load failed. Please try again later.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

